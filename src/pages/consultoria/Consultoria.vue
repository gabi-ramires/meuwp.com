function formatarWhatsapp() {
  const somenteDigitos = whatsapp.value.replace(/\D/g, '')
  let formatado = somenteDigitos

  if (somenteDigitos.length <= 2) {
    formatado = somenteDigitos
  } else if (somenteDigitos.length <= 6) {
    formatado = `(${somenteDigitos.slice(0, 2)}) ${somenteDigitos.slice(2)}`
  } else if (somenteDigitos.length <= 10) {
    formatado = `(${somenteDigitos.slice(0, 2)}) ${somenteDigitos.slice(2, 6)}-${somenteDigitos.slice(6)}`
  } else {
    formatado = `(${somenteDigitos.slice(0, 2)}) ${somenteDigitos.slice(2, 7)}-${somenteDigitos.slice(7, 11)}`
  }

  whatsapp.value = formatado
}

function ajustarBackspace(event) {
  const key = event.key || event.code
  if (key !== 'Backspace') return

  const valor = whatsapp.value
  const posicoesMascara = [' ', '(', ')', '-', '.']
  if (posicoesMascara.includes(valor.slice(-1))) {
    whatsapp.value = valor.slice(0, -1)
  }
}

